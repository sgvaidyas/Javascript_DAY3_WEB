function add(a,b)
{
    var sum = a+b;
    document.write("Sum = " + sum + "<br>");
}
function sub(a,b)
{
    var diff = a-b;
    document.write("Difference = " + diff+ "<br>");
}
function mul(a,b)
{
    var prod = a*b;
    document.write("Product = " + prod+ "<br>");
}
function div(a,b)
{
    var quot = a/b;
    document.write("Quotient = " + quot+ "<br>");
}